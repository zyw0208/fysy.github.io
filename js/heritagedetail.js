// 加载数据
document.addEventListener('DOMContentLoaded', function () {
    const urlParams = new URLSearchParams(window.location.search);
    const itemId = urlParams.get('id');
    const item = heritageData[decodeURIComponent(itemId)];

    if (item) {
        // 更新面包屑导航
        document.getElementById('current-item').textContent = decodeURIComponent(itemId);
        const levelLink = document.querySelector('.breadcrumb a[href*="level="]');
        if (item.level === '省级') {
            levelLink.textContent = '省级非遗';
            levelLink.href = 'details.html?level=provincial';
        } else if (item.level === '国家级') {
            levelLink.textContent = '国家级非遗';
            levelLink.href = 'details.html?level=national';
        } else {
            levelLink.textContent = '市级非遗';
            levelLink.href = 'details.html?level=municipal';
        }

        // 更新基本信息
        document.getElementById('detail-title').textContent = decodeURIComponent(itemId);
        document.getElementById('detail-image').src = item.image;
        document.getElementById('detail-image').alt = decodeURIComponent(itemId);
        document.getElementById('image-caption').textContent = item.caption;
        document.getElementById('detail-category').textContent = item.category;
        document.getElementById('detail-level').textContent = item.level;
        document.getElementById('detail-region').textContent = item.region;
        document.getElementById('detail-description').innerHTML = item.description.replace(/\n\n/g, '</p><p>');


    } else {
        // 如果没有找到对应的项目
        document.getElementById('detail-title').textContent = "项目未找到";
        document.getElementById('detail-description').textContent = "抱歉，没有找到您查询的非遗项目信息。";
    }
})

